public class AddTwoNumbers {
    public static void main(String[] args) {
        // Initialize variables
        int a = 5; // First number
        int b = 10; // Second number

        // Perform addition
        int sum = a + b;

        // Display the result
        System.out.println("The sum of " + a + " and " + b + " is: " + sum);
    }
}
